const SUPPORTED = ["en", "hi", "mr"];

function getLang() {
  const saved = localStorage.getItem("rm-lang");
  if (saved && SUPPORTED.includes(saved)) return saved;
  const nav = (navigator.language || "en").slice(0, 2);
  return SUPPORTED.includes(nav) ? nav : "en";
}

function t(key, lang) {
  const dict = I18N[lang] || I18N.en;
  return dict[key] != null ? dict[key] : (I18N.en[key] != null ? I18N.en[key] : key);
}

function applyLang(lang) {
  document.documentElement.setAttribute("lang", lang);
  localStorage.setItem("rm-lang", lang);

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    el.innerHTML = t(el.getAttribute("data-i18n"), lang);
  });
  document.querySelectorAll("[data-i18n-aria]").forEach((el) => {
    el.setAttribute("aria-label", t(el.getAttribute("data-i18n-aria"), lang));
  });

  document.querySelectorAll(".lang button").forEach((b) => {
    b.classList.toggle("active", b.dataset.lang === lang);
  });
}

window.RM_I18N = { I18N, SUPPORTED, getLang, applyLang };
